// el objeto Math es un objeto global que contiene propiedades y métodos para constantes y funciones matemáticas
// el objeto Math no es un constructor, por lo que no se puede instanciar
// el objeto Math no tiene propiedades, pero tiene métodos y constantes
// el objeto Math tiene propiedades y métodos estáticos, por lo que no se puede crear una instancia de Math
// ejemplo de uso de Math
console.log(Math.PI); // 3.141592653589793
console.log(Math.E); // 2.718281828459045
console.log(Math.SQRT2); // 1.4142135623730951
console.log(Math.SQRT1_2); // 0.7071067811865476    
console.log(Math.LN2); // 0.6931471805599453
console.log(Math.LN10); // 2.302585092994046
console.log(Math.LOG2E); // 1.4426950408889634  
console.log(Math.LOG10E); // 0.4342944819032518
console.log(Math.SQRT3); // 1.7320508075688772
console.log(Math.SQRT1_3); // 0.5773502691896257
console.log(Math.SQRT5); // 2.23606797749979
console.log(Math.SQRT1_5); // 0.4472135954999579
